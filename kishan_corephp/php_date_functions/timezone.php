<?php
date_default_timezone_set("Asia/Calcutta");
echo "Today's Date and Time is :".date("d/m/Y H:i:s a")."<br>"; // It shows the time in 24Hrs Format.

echo "Today's Date & Time is :".date("d-m-y h:i:s a"); // It shows the time in 12 Hrs Format.

/* In this Function here:-

    H / h - It denotes the Hour in 24Hrs / 12 Hrs Format. 

    i - It denotes the minutes.

    s - It denotes the seconds.

    a - It denotes the current am/pm .  */

?>