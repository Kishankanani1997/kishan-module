/*
    LOOP:- Loops in PHP are used to execute the same block of code a specified number of times.


    There are 4 main types of loops:

    1) FOR LOOP - The for loop is used when you know in advance how many times the script should run.

       Syntax - for (initialization; condition; increment/decrement) 
                 {
                 code to be executed;
                 }


    2) WHILE LOOP - The while loop executes a block of code as long as the specified condition is true.

       Syntax - while (condition)
                {
                code to be executed;
                }


    3) DO WHILE LOOP - The do...while loop will always execute the block of code once, it will then check the condition, and repeat the loop while the specified condition is true.

        Syntax -  do
                  {
                  code to be executed;
                  } 
                  while (condition is true);


    4) FOR EACH LOOP - The foreach loop works only on arrays, and is used to loop through each key/value pair in an array.
    
        Syntax - foreach ($array as $value) 
                 {
                 code to be executed;
                }
*/
